package variable;

public class Var7 {
    public static void main(String[] args) {

        //직접 적은 값들을 리터럴이라함
        int a = 100; //정수 리터럴
        double b = 10.5; // 실수 리터럴
        boolean c = true; // true, false 불리언 리터럴
        char d = 'A'; // 문자 하나 리터럴 / 작은따옴표 반드시 사용
        String e = "Hello Java"; // 문자열 리터럴 / 큰 따옴표 사용

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
